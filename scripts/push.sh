#!/bin/sh

# Available environment variables:
#
# DOCKER_IMAGE_NAME
# DOCKER_REGISTRY
# DOCKER_NAMESPACE
# DOCKER_REGISTRY_USERNAME
# DOCKER_REGISTRY_PASSWORD
# DOCKER_TAG

DOCKER_TAG=${DOCKER_TAG:-jenkins-$BUILD_ID}

docker login --username $DOCKER_REGISTRY_USERNAME --password $DOCKER_REGISTRY_PASSWORD $DOCKER_REGISTRY
docker push  $DOCKER_REGISTRY/$DOCKER_NAMESPACE/$DOCKER_IMAGE_NAME:$DOCKER_TAG
